    private void ButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonEnviarActionPerformed
        try
        {
            Model.sendInformation(Integer.parseInt(txtCedula.getText()), txtNombre.getText(), txtArticulo.getText(),
                    Integer.parseInt(txtCantidad.getText()), Integer.parseInt(txtPrecio.getText()), (String) boxPaises.getSelectedItem().toString());
            
            Vector<String> vector = new Vector<String>();
            vector.add(txtCedula.getText());
            vector.add(txtNombre.getText());
            vector.add(txtArticulo.getText());
            vector.add(txtCantidad.getText());
            vector.add(txtPrecio.getText());
            vector.add(boxPaises.getSelectedItem().toString());
            vector.add(""+(Integer.parseInt(txtPrecio.getText())*Integer.parseInt(txtCantidad.getText())));
            modelo.addRow(vector);
        } catch (SQLException ex)
        {
            Logger.getLogger(Interfaz_grafica.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ButtonEnviarActionPerformed

